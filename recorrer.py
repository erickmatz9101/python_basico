##Para recorrer una frase e imprimir caracter por caracter pero que lo devuelva en mayúscula, sería de la siguiente manera:

def run():

    frase = input("Escribe la frase: ")

    for caracter in frase:
        print(caracter.upper())


if __name__  == '__main__':
    run()



